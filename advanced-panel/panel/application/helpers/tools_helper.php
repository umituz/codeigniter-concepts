function convertToSEO($text)
{
	$from = array("ü","Ü","ç","Ç","ğ","Ğ","ö","Ö","İ","ı","ş","Ş",".",",","'","\""," ","!","?","*","_","|","=","(",")","[","]","{","}","%","<",">",";",":","`","/");
	$to    = array("u","u","c","C","g","g","o","o","i","i","s","s","-","-","-","-","-","-","-","-","-","-","-","-","-","-","-","-","-","-","-","-","-","-","-","-");  
	return strtolower(str_replace($from, $to, $text));
}

function pre($array)
{
